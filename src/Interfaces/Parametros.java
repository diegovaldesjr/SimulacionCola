    private void jButtonEliminarProbLlegadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarProbLlegadaActionPerformed

        int filaSelect = jTableLlegadas.getSelectedRow();
        
        if(filaSelect >= 0){
            DefaultTableModel model = (DefaultTableModel) this.jTableLlegadas.getModel();
            model.removeRow(filaSelect);
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione una fila de la tabla");
        }
    }//GEN-LAST:event_jButtonEliminarProbLlegadaActionPerformed

    private void jButtonIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIniciarActionPerformed

        if(jTextFieldDias.getText().equals("") || jTextFieldEstaciones.getText().equals("") ||
            jTextFieldHoras.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Los parametros no pueden estas vacios");
        }else{
            try{
                int dias = Integer.parseInt(jTextFieldDias.getText());
                int horas = Integer.parseInt(jTextFieldHoras.getText());
                int estaciones = Integer.parseInt(jTextFieldEstaciones.getText());
                
                int maxClientes;
                if(jTextFieldMaxClientes.getText().equals("")){
                    maxClientes = Simulacion.Infinito;
                }else{
                    maxClientes = Integer.parseInt(jTextFieldMaxClientes.getText());
                }
                
                if(dias > 0  && horas > 0 && maxClientes > 0 && estaciones > 0){
                    float cont = 0;
                    
                    for(int i=0; i<jTableLlegadas.getRowCount(); i++){
                        cont += Float.parseFloat(jTableLlegadas.getValueAt(i, 1).toString());
                    }
                    
                    if(cont == 1){
                        //Empezar a tomar valores de las estaciones
                        
                        ArrayList tiemposLlegadas = new ArrayList<Tiempo>();
                        
                        for(int i=0; i<jTableLlegadas.getRowCount(); i++){
                            tiemposLlegadas.add(new Tiempo(Integer.parseInt(jTableLlegadas.getValueAt(i, 0).toString()),
                                                Float.parseFloat(jTableLlegadas.getValueAt(i, 1).toString())));
                        }
                        
                        if(tiemposSalidas.size() == estaciones){
                            new Simulacion(dias, horas, estaciones, maxClientes, tiemposLlegadas, tiemposSalidas).setVisible(true);
                            this.dispose();
                        }else{
                            JOptionPane.showMessageDialog(this, "Coloque los tiempos de salida");
                        }
                        
                    }else{
                        JOptionPane.showMessageDialog(this, "La suma de las probabilidades debe ser igual a 1.");
                    }
                }else{
                   JOptionPane.showMessageDialog(this, "Solo se admiten valores positivos"); 
                }

            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(this, "Solo se admiten valores numericos");
            }
        }
    }//GEN-LAST:event_jButtonIniciarActionPerformed

