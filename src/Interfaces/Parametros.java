    private void jButtonAgregarTiempoLlegadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarTiempoLlegadaActionPerformed

        if(jTextFieldTiempoLlegadas.getText().equals("") || jTextFieldProbLlegadas.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Los parametros no pueden estas vacios");
        }else{
            try{
                int tiempo = Integer.parseInt(jTextFieldTiempoLlegadas.getText());
                float probabilidad = Float.parseFloat(jTextFieldProbLlegadas.getText());

                if(tiempo > 0 && probabilidad > 0){
                    String[] llegada = {String.valueOf(tiempo), String.valueOf(probabilidad)};

                    DefaultTableModel model = (DefaultTableModel) this.jTableLlegadas.getModel();
                    model.addRow(llegada);
                }else{
                    JOptionPane.showMessageDialog(this, "Solo se admiten valores positivos");
                }
                
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(this, "Solo se admiten valores numericos");
            }
        }
    }//GEN-LAST:event_jButtonAgregarTiempoLlegadaActionPerformed

